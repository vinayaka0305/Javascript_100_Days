//the find() in js is used to get the first element in the array that matches a condition

//it searches through an array;

//it returns the first matching element;

//if not match is found,it returns undefined


const numbers = [1,2,3,4,5];


console.log(numbers.find((num)=>num>4));


// Key Points:
// ✅ Stops at the first match (doesn't check further).
// ✅ Returns the actual value, not an array.
// ✅ If no match, returns undefined.



