const sentence = "I love coding in JavaScript";
const words = sentence.split(' ');
console.log(words);
// The string "I love coding in JavaScript" is split at every space (' ').
// The result is an array: ["I", "love", "coding", "in", "JavaScript"].

// In simple terms, the split method takes a long string and breaks it into smaller pieces wherever you tell it to, making it easier to work with the individual parts.